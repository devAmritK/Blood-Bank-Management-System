package Beanclasses;

public class DonorsBean {

	public int ID;
	
	public String Name,Email,BloodGroup,PreviousBloodDonationDate,CurrentWeight,QuantityOfBlood,AnyDisease,HemoGlobin,ContactNumber;

	public int getID() {
		return ID;
	}

	public void setID(int iD) {
		ID = iD;
	}

	public String getName() {
		return Name;
	}

	public void setName(String name) {
		Name = name;
	}

	public String getEmail() {
		return Email;
	}

	public void setEmail(String email) {
		Email = email;
	}

	public String getBloodGroup() {
		return BloodGroup;
	}

	public void setBloodGroup(String bloodGroup) {
		BloodGroup = bloodGroup;
	}

	public String getPreviousBloodDonationDate() {
		return PreviousBloodDonationDate;
	}

	public void setPreviousBloodDonationDate(String previousBloodDonationDate) {
		PreviousBloodDonationDate = previousBloodDonationDate;
	}

	public String getCurrentWeight() {
		return CurrentWeight;
	}

	public void setCurrentWeight(String currentWeight) {
		CurrentWeight = currentWeight;
	}

	public String getQuantityOfBlood() {
		return QuantityOfBlood;
	}

	public void setQuantityOfBlood(String quantityOfBlood) {
		QuantityOfBlood = quantityOfBlood;
	}

	public String getAnyDisease() {
		return AnyDisease;
	}

	public void setAnyDisease(String anyDisease) {
		AnyDisease = anyDisease;
	}

	public String getHemoGlobin() {
		return HemoGlobin;
	}

	public void setHemoGlobin(String hemoGlobin) {
		HemoGlobin = hemoGlobin;
	}

	public String getContactNumber() {
		return ContactNumber;
	}

	public void setContactNumber(String contactNumber) {
		ContactNumber = contactNumber;
	}



}
